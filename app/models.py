    short_description =  db.Column(db.String())
    medium_description = db.Column(db.String())
    long_description =   db.Column(db.String())
    price =              db.Column(db.Float())
    shelf_time =         db.Column(db.Integer)
    campus_id =          db.Column(db.Integer)
    last_update =        db.Column(db.Integer)
    archived =           db.Column(db.Integer)
    taxable =            db.Column(db.Integer)
    category_names =     db.Column(db.String())
    vendor =             db.Column(db.String())
    source =             db.Column(db.String())
    notes =              db.Column(db.String())
    total_cal =          db.Column(db.Integer)
    num_servings =       db.Column(db.Integer)
    ingredients =        db.Column(db.String())
    calories =           db.Column(db.Integer)
    proteins =           db.Column(db.Integer)
    sugar =              db.Column(db.Integer)
    carbohydrates =      db.Column(db.Integer)
    fat =                db.Column(db.Integer)
