    kiosk_ship_qty =     db.Column(db.Integer)
    pick_station =       db.Column(db.Integer)
    fc_title =           db.Column(db.String())
    width_space =        db.Column(db.Float())
    height_space =       db.Column(db.Float())
    depth_space =        db.Column(db.Float())
    slotted_width =      db.Column(db.Float())
    tag_volume =         db.Column(db.Integer)
    delivery_option =    db.Column(db.String())
    tag_applied_by =     db.Column(db.String())

